    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $user = User::find($id);
        $user->name = $request->name;
        $user->email = $request->email;
        $user->save();
        return ["status" => true, "message" => "Successfully updated"];
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $user = User::find($id);
        $registers = Register::where('dancers', $user->name)->get();
        
        foreach ($registers as $register) {
            $coach = Coach::where('name', $register->coach)->firstOrFail();
            $coach->available_hours += $register->taked_hours;
            $coach->save();
        }

        Register::where('dancers', $user->name)->delete();
        $user->delete();
        
        return ["status" => true, "message" => "Visitor was successfully deleted"];
    }
